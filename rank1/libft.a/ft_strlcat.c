#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	s;
	size_t	d;
	size_t	ss;
	size_t	dd;

	ss = ft_strlen(src);
	if (!dst && size == 0)
		return (ss);
	d = ft_strlen(dst);
	dd = d;
	if (size <= dd)
		return (size + ss);
	s = 0;
	while (src[s] && d + 1 < size)
	{
		dst[d] = src[s];
		s++;
		d++;
	}
	dst[d] = 0;
	return (dd + ss);
}
